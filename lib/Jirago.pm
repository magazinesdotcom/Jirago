package Jirago;

=head1 NAME

Jirago - Where's Jira going?

=head1 DESCRIPTION

While Jira is an excellent and full-featured bug tracking and reporting system,
it does lack a lot when it comes to figuring out the status of an upcoming
release. They have plenty of tools, and an API but we found that insufficient.
Also, ugly.

Jirago is a simple application inspired by the Panic Board
(L<http://www.panic.com/blog/2010/03/the-panic-status-board/>) to show us
exactly where we are going, how far away we are and what we need to do.

